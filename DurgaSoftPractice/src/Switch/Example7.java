package Switch;

public class Example7 {
	public static void main(String[] args) {
		int x=0;
		switch(x) {
		default:
			System.out.println("def");
		case 0:
			System.out.println("hii");
			break;
		case 1:
			System.out.println("hii 1");
			 
		case 2:
			System.out.println("hii 2");
			
		
		}
	}
}
