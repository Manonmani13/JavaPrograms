package AssignmentOperators;

public class Example1 {
public static void main(String[] args) {
	//simple assignment
	int a=10;
	
//chained assignment
	int a1,b,c,d;
	a1=b=c=d=20;
	System.out.println(a1);
	//compound operators += -= *= /= %= &= |= ^= >>= >>>= <<= 
	int v=10;
	v+=40;
	System.out.println(v);
}
}
